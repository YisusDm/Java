    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        
    try{
        String num = tablefac.getValueAt(tablefac.getSelectedRow(), 0) .toString();
        ConexionBD Con = new ConexionBD();
        Con.getConexion();
        String SQL = "DELETE FROM factura WHERE Id_Factura='"+num+"'";
        Con.sentencia.execute(SQL);
        JOptionPane.showMessageDialog(null, "¡El registro fué eliminado!");
        Con.DesconectarBD();
        } catch (SQLException ex) {
	Logger.getLogger(Factura.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_btneliminarActionPerformed

