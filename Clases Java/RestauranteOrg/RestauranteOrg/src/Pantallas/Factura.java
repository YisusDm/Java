    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
         ConexionBD Con = new ConexionBD();
        Con.getConexion();
        String emp = txtven.getText();
        String can = txtcan.getText();
        String domi = cbdomi.getSelectedItem().toString();
        String me = cbme.getSelectedItem().toString();
        String pro = txtpro.getText();
        String im = txtim.getText();
        String sub = txtsub.getText();
        String pag = txtpag.getText();

        if (!emp.isEmpty()){
            if (!can.isEmpty()) {
                if (!domi.isEmpty()) {
                    if (!me.isEmpty()) {
                        if (!pro.isEmpty()) {
                            if (!im.isEmpty()) {
                                if (!sub.isEmpty()) {
                                    if (!pag.isEmpty()) {
                                      
                                try {
                                        String SQL = "INSERT INTO platillos"
                                                +"(NombrePlatillo,Porciones,Categoria,PrecioPlatillo)"
                                                +" VALUES "
                                                +"('"+emp+"','"+can+"','"+domi+"','"+me+"','"+pro+"','"+im+"','"+sub+"','"+pag+");";
                                        Con.sentencia.execute(SQL);
                                        JOptionPane.showMessageDialog(null, "¡Los datos fueron guardados con exito!");
                                    } catch (SQLException ex) {
                                        Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE,null, ex);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Por favsr ingrese el valor pagar");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor ingrese el subtotsl");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por favor ingrese el valor del impoconsumo");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Por favor ingrese cuanto es la propina");
                    }
                }  else {
                    JOptionPane.showMessageDialog(null, "Por favor ingrese el numero de mesa");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor ingrese el id de los domicilios");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor ingrese la cantidad de platillos");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Por favor ingrese la identificacion del empleado");
    }
     Con.DesconectarBD();   
    }//GEN-LAST:event_btnguardarActionPerformed

