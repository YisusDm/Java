    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        ConexionBD Con = new ConexionBD();
        Con.getConexion();
        String hora = txthor.getText();
        String dia = Integer.toString(fecha.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(fecha.getCalendar().get(Calendar.MONTH)+1);
        String year = Integer.toString(fecha.getCalendar().get(Calendar.YEAR));
        String fi=(year + "-" +mes+ "-"+dia);
        String nmesa = cbmes.getSelectedItem().toString();
        if (!hora.isEmpty()){
            if (!fi.isEmpty()) {
                if (!nmesa.isEmpty()) {
                        try {
                            String SQL = "INSERT INTO reservaciones"
                                +"(Hora,Fecha,NumeroMesa)"
                                +" VALUES "
                                +"('"+hora+"','"+fi+"','"+nmesa+"');";
                                        Con.sentencia.execute(SQL);
                                        JOptionPane.showMessageDialog(null, "¡Los datos fueron guardados con exito!");
                                    } catch (SQLException ex) {
                                        Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE,null, ex);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Por favor ingrese el numero de mesa");
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Por favor ingrese la fecha");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Por ingrese la hora");
                        }
        Con.DesconectarBD();
            
        
    }//GEN-LAST:event_btnguardarActionPerformed

