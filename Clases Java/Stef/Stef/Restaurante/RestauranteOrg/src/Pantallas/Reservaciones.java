    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
       ConexionBD Con = new ConexionBD();
        Con.getConexion();
        txtmes= cbmes.getSelectedItem().toString();
        
        sqlReservaciones modSql = new sqlReservaciones();
        Modelo.Reservaciones mod = new Modelo.Reservaciones();
        String hora = txthor.getText();
        String dia = Integer.toString(fecha.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(fecha.getCalendar().get(Calendar.MONTH)+1);
        String year = Integer.toString(fecha.getCalendar().get(Calendar.YEAR));
        String fi=(year + "-" +mes+ "-"+dia);


        if (txthor.getText().equals("") || fi.equals("") ) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios, debe llenar todos los datos");
        } else {

                        mod.setHora(txthor.getText());
                        mod.setFecha(fi);
                        mod.setNumeroMesa(txtmes);

                        
                        if (modSql.registrar(mod)) {
                            JOptionPane.showMessageDialog(null, "Registro Guardado");
                            Object[] fila = new Object[5];
                            fila[0] = txtmes;
                            fila[1] = txthor.getText();
                            fila[2] = fi;
                            modelo.addRow(fila);
                            limpiar();
                            
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al Guardar");
                            
                        }
            }       
    }//GEN-LAST:event_btnguardarActionPerformed

