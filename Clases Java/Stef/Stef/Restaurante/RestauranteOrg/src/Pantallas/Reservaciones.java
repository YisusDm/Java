    private void tableresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableresMouseClicked
         PreparedStatement ps = null;
        ResultSet rs = null;
        String dia = Integer.toString(fecha.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(fecha.getCalendar().get(Calendar.MONTH)+1);
        String year = Integer.toString(fecha.getCalendar().get(Calendar.YEAR));
        String fi=(year + "-" +mes+ "-"+dia);
        try {
            ConexionBD objCon = new ConexionBD();
            Connection conn = (Connection) objCon.getConexion();

            int Fila = tableres.getSelectedRow();
            String cedula = tableres.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("SELECT Hora, Fecha, NumeroMesa, Cedula1 FROM reservaciones WHERE IdReservaciones=?");
            ps.setString(1, cedula);
            rs = ps.executeQuery();

            while (rs.next()) {
                cbmes.setSelectedItem(rs.getString("N°Mesa"));
                txthor.setText(rs.getString("Hora"));

            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_tableresMouseClicked

