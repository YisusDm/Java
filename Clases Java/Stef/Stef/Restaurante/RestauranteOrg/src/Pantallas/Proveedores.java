    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        ConexionBD Con = new ConexionBD();
        Con.getConexion();
        
        sqlProveedores modSql = new sqlProveedores();
        Modelo.Proveedor mod = new Modelo.Proveedor();


        if (txtid.getText().equals("") || txtnom.getText().equals("") ||
                txtpro.getText().equals("") || txttel.getText().equals("") || txtco.getText().equals("") ) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios, debe llenar todos los datos");
        } else {


                        mod.setIdProveedor (Integer.parseInt(txtid.getText()));
                        mod.setNombre(txtnom.getText());
                        mod.setProductoVenta(txtpro.getText());
                        mod.setTelefono(Integer.parseInt(txttel.getText()));
                        mod.setCorreo(txtco.getText());
                        
                        
                        if (modSql.registrar(mod)) {
                            JOptionPane.showMessageDialog(null, "Registro Guardado");
                            limpiar();
                            
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al Guardar");
                            
                        }
                    

            }
    }//GEN-LAST:event_btnguardarActionPerformed

