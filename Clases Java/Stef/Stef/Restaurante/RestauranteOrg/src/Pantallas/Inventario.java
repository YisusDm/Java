    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
      ConexionBD Con = new ConexionBD();
        Con.getConexion();
        
        sqlInventario modSql = new sqlInventario();
        Modelo.Inventario mod = new Modelo.Inventario();


        if (txtrpro.getText().equals("") || txtnom.getText().equals("") ||
                txtcant.getText().equals("") || txtuni.getText().equals("") || txtvuni.getText().equals("") || txtpro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios, debe llenar todos los datos");
        } else {


                        mod.setNombre(txtnom.getText());
                        mod.setReferenciaProducto(Integer.parseInt(txtrpro.getText()));
                        mod.setNombre(txtnom.getText());
                        mod.setCantidad(Integer.parseInt(txtcant.getText()));
                        mod.setUnidad(txtuni.getText());
                        mod.setValorUnitario(Integer.parseInt(txtvuni.getText()));
                        mod.setIdProveedor(Integer.parseInt(txtpro.getText()));
                        if (modSql.registrar(mod)) {
                            JOptionPane.showMessageDialog(null, "Registro Guardado");
                            limpiar();
                            
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al Guardar");
                            
                        }
                    

            }
    
                                              

                                          
    }//GEN-LAST:event_btnguardarActionPerformed

